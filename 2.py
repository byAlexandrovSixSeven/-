# Задача 2: Параметры помещения
# Рассчитываем геометрические параметры и стоимость покраски стен

# --- Размеры помещения (в метрах) ---
length = 3.5    # длина (исправлена опечатка: было lenght)
width = 2.5     # ширина
height = 3.3423231  # высота

# --- Вычисление площадей и объёма ---
pl_pola = length * width                        # площадь пола
pl_sten1 = height * length                      # площадь двух длинных стен (одна)
pl_sten2 = height * width                       # площадь двух коротких стен (одна)
obsch_pl_sten = (pl_sten1 + pl_sten2) * 2       # общая площадь всех четырёх стен
obem = length * width * height                  # объём помещения

# --- Стоимость покраски ---
st_pok_m = 125                                  # цена покраски за 1 м² (руб.)
itog_stoim = st_pok_m * obsch_pl_sten           # итоговая стоимость покраски

# --- Вывод результатов ---
print('=== Параметры помещения ===')
print(f'Площадь пола:   {round(pl_pola, 2)} м²')
print(f'Площадь стен:   {round(obsch_pl_sten, 2)} м²')
print(f'Объём:          {round(obem, 2)} м³')
print(f'Стоимость покраски стен: {round(itog_stoim, 2)} руб.')
