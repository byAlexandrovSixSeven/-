# Задача 7: Прайс-лист материалов
# Работа со словарём: добавление, изменение, удаление, подсчёт средней цены

# --- Исходный прайс-лист (5 материалов с ценами) ---
mat = {'mel': 10, 'klay': 20, 'shmel': 23, 'jmel': 100, 'vasyan': 0}

# --- Добавляем два новых материала ---
mat['lak'] = 15
mat['cheremsha'] = 67

# --- Увеличиваем цену 'klay' на 10% ---
mat['klay'] = mat['klay'] * 1.1

# --- Удаляем материал 'jmel' из прайса ---
mat.pop('jmel')

# --- Рассчитываем среднюю цену по всем позициям ---
cen_sr = sum(mat.values()) / len(mat)

# --- Вывод результатов ---
print(f'Средняя цена: {round(cen_sr, 2)} руб.')
print('Итоговый прайс-лист:', mat)
