# Задача 3: Конвертер температур
# Переводим температуру из Цельсия в Фаренгейты и определяем агрегатное состояние воды

# --- Исходная температура ---
grad_cel = 0  # температура в градусах Цельсия

# --- Перевод по формуле: F = C × 9/5 + 32 ---
grad_far = grad_cel * 9 / 5 + 32

# --- Вывод температуры в обоих единицах ---
print(f'Температура: {grad_cel}°C = {grad_far}°F')

# --- Определение агрегатного состояния воды ---
# Границы: лёд — до 0°C, жидкость — 1..99°C, пар — от 100°C
if grad_cel <= 0:
    print('Состояние воды: Лёд')
elif grad_cel < 100:   # исправлено: было 1–99, пропускало 100°C (граница пара)
    print('Состояние воды: Жидкость')
else:
    print('Состояние воды: Пар')
